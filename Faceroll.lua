        elseif stormkeeper > 0 then
            sendKeyToWow("=") -- stormkeeper

        elseif flameshock_on_target == 0 then
            sendKeyToWow("-") -- flame shock

        elseif enough_maelstrom > 0 then
            sendKeyToWow("0") -- earth shock

        else
